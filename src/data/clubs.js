/** All unique tags from clubs for filter UI */
export function getAllTags() {
  const set = new Set();
  clubs.forEach((c) => {
    (c.Club_Tags || "")
      .split(",")
      .map((t) => t.trim())
      .filter(Boolean)
      .forEach((t) => set.add(t));
  });
  return Array.from(set).sort();
}
